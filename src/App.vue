<template>
  <div id="app">
    <input type="text" v-model="name">
    <button v-on:click="format">Format</button>
    <p>{{formattedName}}</p>
  </div>
</template>

<script>
export default {
  name: "app",

  data() {
    return {
      name: "",
      formattedName: ""
    };
  },

  methods: {
    format() {
      this.formattedName = this.name
        .split(" ")
        .join("-")
        .toUpperCase();
    }
  }
};
</script>


